                                    Las bolas tienen diferentes pesos y depende de ti escoger una que puedas lanzar sin tener que hacer demasiado esfuerzo, de modo que es preferible elegir algo que puedas levantar y balancear con facilidad. Para saber cuál es la bola indicada para ti, juega un par de veces y prueba con diferentes bolas hasta encontrar el peso indicado.</p>
                                <p><b>ZAPATOS DE BOWLING</b><br>
                                <p>Los zapatos de bowling están diseñados para permitir un mejor lanzamiento debido a su efecto antideslizante, por ello, tendrás la posición idónea.</p>
                                <p><b>DÓNDE PARARSE</b><br>
                                <p>El lugar donde te pares para comenzar el movimiento influye directamente sobre la dirección que tomará la bola. Debes experimentar y encontrar la mejor posición para el tiro.</p>
                                <p><b>POSICIÓN AL LANZAR</b><br>
                                <p>El inicio del lanzamiento es la caminata. En unos pocos pasos, tu lanzamiento ganará impulso. Si eres diestro, el primer paso deberás darlo con el pie derecho y terminar la caminata con el pie izquierdo. Si eres zurdo, viceversa.</p>
                                <p><b>APUNTAR</b><br>
                                <p>Antes de comenzar la caminata, mira con atención a los pinos y apunta. Hay un grupo de flechas pintadas en el carril justo después de la línea de lanzamiento. En el primer lanzamiento, cuando los diez pinos están alineados, mira el grupo más cercano y elige una de estas flechas. Asegúrate de que el bolo pase por la flecha deseada. En el segundo lanzamiento, apunta directo a los pinos en lugar de hacerlo a las flechas. Cuando apuntes a los pinos, no les quites la vista en ningún punto del movimiento, observarlos desde que se inicia la caminata hasta que sueltes el bolo.</p>
                                <p><b>DOBLAR LAS RODILLAS</b><br>
                                <p>Cuando sueltes la bola, dobla las rodillas lo más que puedas. Esto hará que la bola salga desde un punto más cercano al piso, lo que te brinda un mayor control.</p>
